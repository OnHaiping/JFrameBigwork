package Bean;

public class Client {
    private int clId;
    private String clName;
    private String clSex;
    private int clAge;
    private String clPlace;
    private String clNum;
    private String clRecord;

    public Client() {
    }

    public int getClId() {
        return clId;
    }

    public void setClId(int clId) {
        this.clId = clId;
    }

    public String getClName() {
        return clName;
    }

    public void setClName(String clName) {
        this.clName = clName;
    }

    public String getClSex() {
        return clSex;
    }

    public void setClSex(String clSex) {
        this.clSex = clSex;
    }

    public int getClAge() {
        return clAge;
    }

    public void setClAge(int clAge) {
        this.clAge = clAge;
    }

    public String getClPlace() {
        return clPlace;
    }

    public void setClPlace(String clPlace) {
        this.clPlace = clPlace;
    }

    public String getClNum() {
        return clNum;
    }

    public void setClNum(String clNum) {
        this.clNum = clNum;
    }

    public String getClRecord() {
        return clRecord;
    }

    public void setClRecord(String clRecord) {
        this.clRecord = clRecord;
    }
}
